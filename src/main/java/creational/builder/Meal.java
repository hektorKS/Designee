package creational.builder;

class Meal {

  private String drink;
  private String main;
  private String dessert;

  String getDrink() {
    return drink;
  }

  void setDrink(String drink) {
    this.drink = drink;
  }

  String getMain() {
    return main;
  }

  void setMain(String main) {
    this.main = main;
  }

  String getDessert() {
    return dessert;
  }

  void setDessert(String dessert) {
    this.dessert = dessert;
  }
}
